// --- normalized helper fields (backwards-compatible) ---
// Adds: mainCategory ("Veg" | "Non-Veg"), subCategory (e.g. "Biryani"), lowestPrice (number)
products.forEach((p) => {
  // main category (Veg / Non-Veg)
  p.mainCategory = p.hasOwnProperty("isVeg")
    ? p.isVeg
      ? "Veg"
      : "Non-Veg"
    : /\bVeg\b/i.test(p.category)
      ? "Veg"
      : "Non-Veg";

  // derive subCategory by stripping trailing 'Veg' / 'Non Veg' from the existing category string
  p.subCategory = p.category
    ? p.category.replace(/\s+(Veg|Non\s?Veg)$/i, "").trim()
    : "";

  // compute a robust price to use for sorting (lowest available price for the product)
  const prices = [];
  if (Array.isArray(p.types))
    prices.push(
      ...p.types.map((t) => t.price).filter((v) => typeof v === "number"),
    );
  if (typeof p.priceHalf === "number") prices.push(p.priceHalf);
  if (typeof p.priceFull === "number") prices.push(p.priceFull);
  if (typeof p.price === "number") prices.push(p.price);
  p.lowestPrice = prices.length ? Math.min(...prices) : Infinity;
});
